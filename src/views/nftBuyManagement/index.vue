        <template slot-scope="scope">
          <span
            v-if="scope.row.currentStatus == 'IN_PROGRESS' && scope.row.currentStatus !== 'CANCELLED'"
            class="blueColor publick-button cursor"
            @click="operatingNft(scope.row)"
          >
            {{ scope.row.upAndDown == "down" ? "" : "下架" }}
          </span>
